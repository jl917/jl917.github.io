USE shop;

-- ---------------------------------------------------------------------
-- [정답 1] 품절 상품 찾기
--   ENUM 컬럼도 문자열 리터럴로 비교합니다. 따옴표를 잊지 마세요.
--   결과: 4번(울 니트 스웨터), 27번(노르웨이 연어 필렛) — 둘 다 stock = 0
-- ---------------------------------------------------------------------
SELECT product_id, name, stock
FROM products
WHERE status = 'SOLD_OUT';

-- ---------------------------------------------------------------------
-- [정답 2] 포인트 상위 3명 (한글 별칭)
--   한글 별칭은 백틱 없이도 되지만, 공백/기호가 섞이면 백틱이 필요합니다.
--   결과: 양현우(22400) / 강소라(20100) / 송민지(17300) — 셋 다 VIP
-- ---------------------------------------------------------------------
SELECT
    name   AS 이름,
    grade  AS 등급,
    points AS 포인트
FROM customers
ORDER BY points DESC
LIMIT 3;

-- ---------------------------------------------------------------------
-- [정답 3] 배송 도시 목록
--   8개 나옵니다. customers.city 와 동일합니다.
--   03_seed_orders.sql 이 shipping_city 를 고객의 city 로 그대로 복사했기
--   때문입니다. "주문 데이터의 도시 목록"과 "고객 마스터의 도시 목록"이
--   같은지 확인하는 것은 실무에서 흔한 데이터 정합성 점검입니다.
-- ---------------------------------------------------------------------
SELECT DISTINCT shipping_city FROM orders;

-- ---------------------------------------------------------------------
-- [정답 4] 고가 + 재고부족 상품
--   AND 로 두 조건을 겹칩니다. 9건이 나옵니다.
--   (게이밍노트북 / 울트라북 i7·i5 / 스마트폰 X20 Pro / 원목식탁 /
--    인체공학 의자 / 트렌치코트 / 첼시부츠 / 라이트 다운 재킷)
--   고가 상품일수록 재고를 적게 잡아둔다는 사실이 한눈에 보입니다.
-- ---------------------------------------------------------------------
SELECT product_id, name, price, stock
FROM products
WHERE price >= 100000
  AND stock < 30
ORDER BY price DESC;

-- ---------------------------------------------------------------------
-- [정답 5] 연봉 4위 사원
--   "N번째"는 (N-1)건을 건너뛰고 1건 = LIMIT 1 OFFSET 3.
--   결과: 오지원(경영지원 본부장, 8,800,000)
--
--   [주의] 동점(tie)이 있으면 OFFSET 방식은 위험합니다. 여기서는 salary 가
--   전부 다르지만, 실무에선 RANK()/DENSE_RANK() 윈도우 함수(Step 12)를
--   쓰는 편이 훨씬 안전합니다.
-- ---------------------------------------------------------------------
SELECT employee_id, name, dept, position, salary
FROM employees
ORDER BY salary DESC
LIMIT 1 OFFSET 3;

-- ---------------------------------------------------------------------
-- [정답 6] 재고 자산가치 TOP 5
--   ORDER BY 에서는 SELECT 별칭(stock_value)을 쓸 수 있습니다.
--   product_id 를 두 번째 정렬 키로 붙여 동점 시 순서를 고정합니다.
--   결과 1위: 스마트폰 X20 256GB (1,150,000 x 50 = 57,500,000)
-- ---------------------------------------------------------------------
SELECT
    product_id,
    name,
    price * stock AS stock_value
FROM products
ORDER BY stock_value DESC, product_id ASC
LIMIT 5;

-- ---------------------------------------------------------------------
-- [정답 7] NULL 을 뒤로 보내는 정렬
--   MySQL 에는 NULLS LAST 가 없으므로 (phone IS NULL) 을 첫 번째 정렬 키로
--   씁니다. 값이 있으면 0, NULL 이면 1 → ASC 정렬 시 값 있는 행이 앞으로.
--   그 다음 created_at DESC 로 최신순.
--   결과: 하준서 → 우예린 → 전소민 → 배채영 → 양현우 → 구세진
--   [핵심] 심준호는 created_at 이 2024-03-03 이라 "최신순"만 보면 2위입니다.
--         하지만 phone 이 NULL 이라 첫 번째 정렬 키가 1 이 되어 맨 뒤로 밀립니다.
--         → 상위 6명 안에 안 보이는 것이 정상입니다.
-- ---------------------------------------------------------------------
SELECT customer_id, name, phone, created_at
FROM customers
ORDER BY (phone IS NULL) ASC, created_at DESC
LIMIT 6;

-- ---------------------------------------------------------------------
-- [정답 8] 에러 고치기
--   원인: 논리적 실행 순서가 FROM → WHERE → ... → SELECT 이므로,
--         WHERE 가 평가될 때 SELECT 의 별칭 bonus 는 아직 존재하지 않습니다.
--         → ERROR 1054 (42S22): Unknown column 'bonus' in 'where clause'
--
--   [고친 쿼리 ①] 식을 그대로 반복
-- ---------------------------------------------------------------------
SELECT customer_id, name, points * 2 AS bonus
FROM customers
WHERE points * 2 > 20000
ORDER BY bonus DESC;

-- ---------------------------------------------------------------------
--   [고친 쿼리 ②] 파생 테이블로 감싸기
--     바깥 쿼리 입장에서 bonus 는 이미 "t 라는 테이블의 컬럼" 이므로
--     WHERE 에서 자유롭게 쓸 수 있습니다. 파생 테이블에는 별칭이 필수입니다.
--   두 쿼리 모두 결과 동일: 양현우(44800) / 강소라(40200) / 송민지(34600) /
--                          장혜원(31600) / 김민수(25000)
-- ---------------------------------------------------------------------
SELECT *
FROM (
    SELECT customer_id, name, points * 2 AS bonus
    FROM customers
) AS t
WHERE t.bonus > 20000
ORDER BY t.bonus DESC;
